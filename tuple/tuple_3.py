# Вывести статистику частности букв в кортеже
# Статистика частности - число повторений каждой из букв.

long_word = ('т', 'т', 'а', 'и', 'и', 'а', 'и', 'и', 'и', 'т', 'т', 'а', 'и', 'и', 'и', 'и', 'и', 'т', 'и')

print('Статистика частности буквы "т":', long_word.count('т'))
print('Статистика частности буквы "а":', long_word.count('а'))
print('Статистика частности буквы "и":', long_word.count('и'))

